 private void loadTable() throws SQLException, ClassNotFoundException
   {
      DAOPaciente dp=new DAOPaciente();
      model = new DefaultTableModel(null,titles); 
      
      for(PatientDoctor patientdoctor:dp.getPatientInfo())
      {
          registros[0]=patientdoctor.getSecurityNumber();
          registros[1]=patientdoctor.getFirstname();
          registros[2]=patientdoctor.getLastname();
          registros[3]=Integer.toString(patientdoctor.getAge());
          registros[4]=patientdoctor.getAddress();
          registros[5]=patientdoctor.getTel();
          registros[6]=patientdoctor.getEmail();
          registros[7]=patientdoctor.getWeight();
          registros[8]=patientdoctor.getSize();
          registros[9]=patientdoctor.getDisease();
          registros[10]=Integer.toString(patientdoctor.getRoomid());
          registros[11]=patientdoctor.getIddoctor();
          model.addRow(registros);
         
      }
      
      jTable1.setModel(model);
      jTable1.getColumnModel().getColumn(0).setPreferredWidth(50);
       jTable1.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(100);
         jTable1.getColumnModel().getColumn(3).setPreferredWidth(100);
          jTable1.getColumnModel().getColumn(4).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(100);
         jTable1.getColumnModel().getColumn(6).setPreferredWidth(100);
          jTable1.getColumnModel().getColumn(7).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(8).setPreferredWidth(100);
         jTable1.getColumnModel().getColumn(9).setPreferredWidth(100);
          jTable1.getColumnModel().getColumn(10).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(11).setPreferredWidth(100);
        
      
   }
   

